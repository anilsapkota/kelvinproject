//let us assign a variable kelvin to be of value 293
let kelvin =0;

//convert kelvin to celcius and storing it in a variable
let celcius = kelvin -273; 

//calculate the fahrenheit from the celcius and storing it in a variable and flooring it. 
let fahrenheit = Math.floor(celcius *(9/5) +32);

//loging out the value in the console
console.log(`The temperature is ${fahrenheit} degrees Fahrenheit`);
